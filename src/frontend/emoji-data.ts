let seg: Intl.Segmenter | null = null;
function graphemes(s: string): string[] {
  if (typeof Intl !== 'undefined' && 'Segmenter' in Intl) {
    seg ??= new Intl.Segmenter(undefined, { granularity: 'grapheme' });
    return Array.from(seg.segment(s), (x) => x.segment);
  }
  return Array.from(s);
}

export type EmojiCategory = { name: string; emoji: string[] };

export const EMOJI_CATEGORIES: EmojiCategory[] = RAW.map(([name, s]) => ({
  name,
  emoji: graphemes(s),
}));

export const ALL_EMOJI: string[] = EMOJI_CATEGORIES.flatMap((c) => c.emoji);
